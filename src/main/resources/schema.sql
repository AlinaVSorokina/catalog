

create table REGION(
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ISO_3166_1_ALPHA_2 VARCHAR(128),
    ISO_3166_1_ALPHA_2_REGION_CODE VARCHAR(128),
    REGION1 VARCHAR(128),
    REGION2 VARCHAR(128),
    REGION3 VARCHAR(128),
    REGION4 VARCHAR(128),
    POSTCODE VARCHAR(128),
    ORT VARCHAR(128),
    AREA1 VARCHAR(128),
    AREA2 VARCHAR(128),
    LATITUDE VARCHAR(128),
    LONGITUDE VARCHAR(128),
    TIMEZONE VARCHAR(128),
    UTC VARCHAR(128),
    SUMMERTIME VARCHAR(128),
    ACTIVE VARCHAR(128)
)
AS SELECT * FROM CSVREAD('classpath:postcodes.csv');